# Dictionary

print("***DICTIONARY 1***")
emails = {}
emails["Sanjib"] = "sanjib.s@live.com"
emails["Elon"] = "elon@tesla.com"
print(emails)

print("***DICTIONARY 2***")
phones = {'Sanjib': '+919908212345', 'Elon': '+19900011122', 10: 'No Number'}
print(phones)

print("***ALL KEYS***")
print(emails.keys())

print("***ALL VALUES***")
print(emails.values())

print("***GET A KEY VALUE***")
print(phones[10])
